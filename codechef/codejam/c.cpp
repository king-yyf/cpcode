    int rem=m-sm%m;
    if(rem==0)rem=m;
    sort(all(s));
    for(int i=0;i<m;++i){
    	if(i<rem){
    		ans+=abs(ab-s[i]);
    	}else ans+=abs(ab+1-s[i]);
    }

