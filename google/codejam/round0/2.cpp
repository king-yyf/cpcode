void solve(){
	rd(a1,b1,c1,d1);
	rd(a2,b2,c2,d2);
	rd(a3,b3,c3,d3);
	int a=min(a1,min(a2,a3));
	int b=min(b1,min(b2,b3));
	int c=min(c1,min(c2,c3));
	int d=min(d1,min(d2,d3));
	if(a+b+c+d<N) {
		wt("IMPOSSIBLE\n");
		return;
	}
	int s = 0, n = N;
	a=min(a,n-s);
	s += a;
	if(s<n) {
		b = min(b,n-s);
		s+=b;
	}else b=0;
	if(s<n) {
		c = min(c,n-s);
		s+=c;
	}else c=0;
	if(s<n) {
		d = min(d,n-s);
		s+=d;
	}else d=0;
 
	wt(a,' ',b,' ',c,' ',d,'\n');
}             
