template <typename T>
struct FenwickTree {
    int n;
    vector<T> a;
    static int lg(unsigned x) {return x == 0 ? -1 : 31 - __builtin_clz(x);}
    FenwickTree(int n) : n(n), a(n) {}
    FenwickTree(vector<T> &A) : FenwickTree((int)A.size()) {
        for (int i = 0; i < n; ++i) add(i, A[i]);
    }
    void add(int x, T v) {
        for (int i = x + 1; i <= n; i += i & -i) a[i - 1] += v;
    }
    T sum(int x) {
        T ans{};
        for (int i = min(x + 1, n); i > 0; i -= i & -i) ans += a[i - 1];
        return ans;
    }
    T sum(int l, int r) { //sum[l..r]
        return l <= r ? sum(r) - sum(l - 1) : T{}; 
    }
    T get(int x) const {   // 0 <= x < n, get a[x] in O(1), Equivalent to sum(x, x).
        T s = a[x];
        int y = x + 1;
        y -= y & -y;
        while (x != y) s -= a[x - 1], x -= x & -x;
        return s;
    }
    void set(int x, T v) {  // 0 <= x < n, set a[x] = v
        add(x, v - get(x));
    }
    int kth(T k) const {
        if (k < T()) return -1;
        int x = 0;
        for (int i = 1 << lg(n); i; i /= 2) {
            if (x + i <= n && k >= a[x + i - 1]) {
                x += i, k -= a[x - 1];
            }
        }
        return x;
    }
};

class Solution {
public:
    vector<int> countOfPeaks(vector<int>& a, vector<vector<int>>& qs) {
        n=sz(a),m=sz(qs);
        FenwickTree<int> f(n);
        for(int i=1;i<n-1;++i){
        	if(a[i]>a[i-1]&&a[i]>a[i+1])f.set(i,1);
        }
        vector<int> c;
        for(auto&q:qs){
        	// wt(a);
        	if(q[0]==1){
        		int l=q[1],r=q[2];
        		if(r-l<2){
        			c.push_back(0);
        		}else{
        			c.push_back(f.sum(l+1,r-1));
        		}
        	}else{
        		int p=q[1],x=q[2];

        		if(f.get(p)==1){
        			f.set(p,0);
        		}
        		if(p>0&&f.get(p-1)==1){
        			f.set(p-1,0);
        		}
        		if(p<n-1&&f.get(p+1)==1){
        			f.set(p+1,0);
        		}


        		a[p]=x;

        		if(p>0&&p<n-1 && a[p]>a[p-1]&&a[p]>a[p+1]){
        			f.set(p,1);
        		}
        		if(p-1>0 && a[p-1]>a[p]&&a[p-1]>a[p-2]){
        			f.set(p-1,1);
        		}
        		if(p+1<n-1 && a[p+1]>a[p]&&a[p+1]>a[p+2]){
        			f.set(p+1,1);
        		}
        	}
        }
        return c;
    }
};

