class Solution {
public:
	string add(string x, string a){
        string u = x + a;
        int n=sz(x);
        if(x[n-1] == a[0]) u = x.substr(0,n) + a.substr(1, a.length()-1);
        return u;
    }
    unordered_map<int,string> dp;
    string f(int i, vector<string> &a){
        if(i == 0)return a[0];
        if(dp.count(i)) return dp[i];
        string x = f(i-1, a),u = add(x, a[i]), v = add(a[i], x);
        if(u.length() < v.length()) return dp[i] = u;
        if(u.length() > v.length()) return dp[i] = v;
        int n = u.size(), m = v.size();
        if(u[n-1] == a[i+1][0] || u[0] == a[i+1][a.size()-1]){
            return dp[i]=u;
        }
        return dp[i] = v;
    }
    int minimizeConcatenatedLength(vector<string>& a) {
        n=sz(a);a.push_back("$");
        return sz(f(n-1,a));
    }
};

