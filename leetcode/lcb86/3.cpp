void dfs(vector<int>& nums, int step) {
    if (tmp.size() >= K) {
        ret.push_back(tmp);
        return;
    }
    for (int i = step; i < nums.size(); i++) {
        tmp.push_back(nums[i]);  // nums[i] == i，所以这里直接 push(i) 也可以
        dfs(nums, i + 1);
        tmp.pop_back();
    }
}

